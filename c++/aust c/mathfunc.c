#include <stdio.h>
#include <math.h>

int main()
{
    printf("SQRT%fn",sqrt(10.0));
    printf("Exp%f\n",exp(4.0));
    printf("LOg%f\n",log(4.0));
    printf("LOg10%f\n",log10(100.0));
    printf("FAbs%f\n",fabs(-5.2));
    printf("CEli%f\n",ceil(4.5));
    printf("Floor%f\n",floor(-4.5));
    printf("Power%f\n",pow(4.0,.5));
    printf("Fmod%f\n",fmod(4.5,2.0));
    printf("SIN%f\n",sin(0.0));
    printf("COS%f\n",cos(0.0));
    printf("TAN%f\n",tan(0.0));
    return 0;
}